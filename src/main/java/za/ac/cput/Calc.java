package za.ac.cput;

public class Calc {
    /**
     * adds two integers
     * @param a int
     * @param b int
     * @return int
     */
    public static int add(int a, int b) {
        return (a + b);
    }

    /**
     * adds two real numbers
     * @param a double
     * @param b double
     * @return double
     */
    public static double add(double a, double b) {
        return (a + b);
    }
}
